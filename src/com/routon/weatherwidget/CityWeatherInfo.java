package com.routon.weatherwidget;

import java.util.ArrayList;

public class CityWeatherInfo {
	private int cid;
	private ArrayList<WeatherIndex> index_weathers;
	private ArrayList<WeatherDetail> city_weathers;

	public CityWeatherInfo()
	{
		city_weathers = new ArrayList<WeatherDetail>();
		index_weathers = new ArrayList<WeatherIndex>();
	}
	
	public int getCid()
	{
		return this.cid;
	}
	
	public void setCid(int cid)
	{
		this.cid = cid;
	}
	
	public ArrayList<WeatherDetail> getCityWeather()
	{
		return this.city_weathers;
	}
	
	public ArrayList<WeatherIndex> getWeatherIndex()
	{
		return this.index_weathers;
	}
}
